@dataclass
class Variable(object):
    cube: Cube

    @property
    def level(self) -> int:
        return int(self.cube.code.split('_')[0][-1])

    @property
    def workspace(self) -> WAPORVersion:
        if self.level == 'L3':
            return wapor_v1
        return wapor_v2


