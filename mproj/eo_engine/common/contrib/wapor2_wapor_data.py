class BBox:
    min_x: float
    min_y: float

    max_x: float
    max_y: float

    projection: int = 4326

    def as_shape(self):
        return {
            "crs": "EPSG:%d" % self.projection,
            "type": "Polygon",
            "coordinates": [
                [
                    [
                        self.min_x,
                        self.max_y
                    ],
                    [
                        self.max_x,
                        self.max_y
                    ],
                    [
                        self.max_x,
                        self.min_y
                    ],
                    [
                        self.min_x,
                        self.min_y
                    ],
                    [
                        self.min_x,
                        self.max_y
                    ]
                ]
            ]
        }
